package agh.ics.oop;

import org.junit.jupiter.api.Test;

import java.util.List;

import static org.junit.jupiter.api.Assertions.*;

class SimulationEngineTest {
    @Test
    void RectangularMapRenderingTest() {
        String[] args = new String[]{"f", "b", "r", "l", "f", "f", "r", "r", "f", "f", "f", "f", "f", "f", "f", "f"};
        MoveDirection[] directions = new OptionsParser().parse(args);
        IWorldMap map = new RectangularMap(10, 5);
        Vector2d[] positions = {new Vector2d(2, 2), new Vector2d(3, 4)};
        IEngine engine = new SimulationEngine(directions, map, positions);
        engine.run();
        System.out.println(map);
        List<Animal> animals = map.getAnimals();
        assertEquals(animals.get(0).getOrientation(), MapDirection.SOUTH);
        assertTrue(animals.get(0).isAt(new Vector2d(2,0)));
        assertEquals(animals.get(1).getOrientation(), MapDirection.NORTH);
        assertTrue(animals.get(1).isAt(new Vector2d(3,5)));

    }
}